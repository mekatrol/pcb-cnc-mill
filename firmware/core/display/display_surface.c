static const uint8_t *font5x7(char ch)
{
  static const uint8_t glyphs[][5] = {
      {0x00, 0x00, 0x00, 0x00, 0x00}, // space
      {0x3E, 0x51, 0x49, 0x45, 0x3E}, // 0
      {0x00, 0x42, 0x7F, 0x40, 0x00}, // 1
      {0x42, 0x61, 0x51, 0x49, 0x46}, // 2
      {0x21, 0x41, 0x45, 0x4B, 0x31}, // 3
      {0x18, 0x14, 0x12, 0x7F, 0x10}, // 4
      {0x27, 0x45, 0x45, 0x45, 0x39}, // 5
      {0x3C, 0x4A, 0x49, 0x49, 0x30}, // 6
      {0x01, 0x71, 0x09, 0x05, 0x03}, // 7
      {0x36, 0x49, 0x49, 0x49, 0x36}, // 8
      {0x06, 0x49, 0x49, 0x29, 0x1E}, // 9
      {0x7E, 0x11, 0x11, 0x11, 0x7E}, // A
      {0x7F, 0x49, 0x49, 0x49, 0x36}, // B
      {0x3E, 0x41, 0x41, 0x41, 0x22}, // C
      {0x7F, 0x41, 0x41, 0x22, 0x1C}, // D
      {0x7F, 0x49, 0x49, 0x49, 0x41}, // E
      {0x7F, 0x09, 0x09, 0x09, 0x01}, // F
      {0x3E, 0x41, 0x49, 0x49, 0x7A}, // G
      {0x7F, 0x08, 0x08, 0x08, 0x7F}, // H
      {0x00, 0x41, 0x7F, 0x41, 0x00}, // I
      {0x20, 0x40, 0x41, 0x3F, 0x01}, // J
      {0x7F, 0x08, 0x14, 0x22, 0x41}, // K
      {0x7F, 0x40, 0x40, 0x40, 0x40}, // L
      {0x7F, 0x02, 0x0C, 0x02, 0x7F}, // M
      {0x7F, 0x04, 0x08, 0x10, 0x7F}, // N
      {0x3E, 0x41, 0x41, 0x41, 0x3E}, // O
      {0x7F, 0x09, 0x09, 0x09, 0x06}, // P
      {0x3E, 0x41, 0x51, 0x21, 0x5E}, // Q
      {0x7F, 0x09, 0x19, 0x29, 0x46}, // R
      {0x46, 0x49, 0x49, 0x49, 0x31}, // S
      {0x01, 0x01, 0x7F, 0x01, 0x01}, // T
      {0x3F, 0x40, 0x40, 0x40, 0x3F}, // U
      {0x1F, 0x20, 0x40, 0x20, 0x1F}, // V
      {0x3F, 0x40, 0x38, 0x40, 0x3F}, // W
      {0x63, 0x14, 0x08, 0x14, 0x63}, // X
      {0x07, 0x08, 0x70, 0x08, 0x07}, // Y
      {0x61, 0x51, 0x49, 0x45, 0x43}, // Z
      {0x00, 0x36, 0x36, 0x00, 0x00}, // :
      {0x08, 0x08, 0x08, 0x08, 0x08}, // -
      {0x00, 0x60, 0x60, 0x00, 0x00}, // .
      {0x63, 0x13, 0x08, 0x64, 0x63}, // %
      {0x02, 0x01, 0x51, 0x09, 0x06}, // ?
  };

  if (ch >= 'a' && ch <= 'z')
  {
    ch = (char)(ch - 'a' + 'A');
  }
  if (ch == ' ')
  {
    return glyphs[0];
  }
  if (ch >= '0' && ch <= '9')
  {
    return glyphs[1 + (uint8_t)(ch - '0')];
  }
  if (ch >= 'A' && ch <= 'Z')
  {
    return glyphs[11 + (uint8_t)(ch - 'A')];
  }
  if (ch == ':')
  {
    return glyphs[37];
  }
  if (ch == '-')
  {
    return glyphs[38];
  }
  if (ch == '.')
  {
    return glyphs[39];
  }
  if (ch == '%')
  {
    return glyphs[40];
  }
  if (ch == '?')
  {
    return glyphs[41];
  }
  return glyphs[0];
}

uint16_t display_rgb565(uint8_t red, uint8_t green, uint8_t blue)
{
  return (uint16_t)(((uint16_t)(red & 0xF8u) << 8) |
                    ((uint16_t)(green & 0xFCu) << 3) |
                    ((uint16_t)blue >> 3));
}

static void display_surface_draw_char(const display_surface_t *surface, uint16_t x, uint16_t y,
                                      char ch, uint16_t foreground, uint16_t background,
                                      uint8_t scale)
{
  const uint8_t *glyph = font5x7(ch);

  for (uint8_t col = 0; col < 6; col++)
  {
    const uint8_t bits = (col < 5) ? glyph[col] : 0;
    for (uint8_t row = 0; row < 8; row++)
    {
      const uint16_t color = (bits & (uint8_t)(1u << row)) ? foreground : background;
      surface->fill_rect(surface->context, (uint16_t)(x + col * scale),
                         (uint16_t)(y + row * scale), scale, scale, color);
    }
  }
}

void display_surface_draw_text(const display_surface_t *surface, uint16_t x, uint16_t y,
                               const char *text, uint16_t foreground, uint16_t background,
                               uint8_t scale)
{
  while (*text != '\0')
  {
    display_surface_draw_char(surface, x, y, *text, foreground, background, scale);
    x = (uint16_t)(x + 6u * scale);
    text++;
  }
}
